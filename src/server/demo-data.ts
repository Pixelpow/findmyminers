function sparkline(base: number, variance: number, points = 48): number[] {
  return Array.from({ length: points }, (_, i) =>
    Math.max(0, base + (Math.sin(i * 0.4) * variance * 0.6) + (Math.random() - 0.5) * variance)
  );
}

